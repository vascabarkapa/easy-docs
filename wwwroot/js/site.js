function updateDateTime() {
    var now = new Date();
    var dateTimeString = now.toLocaleString('en-US', {
        weekday: 'long',
        year: 'numeric',
        month: 'long',
        day: 'numeric',
        hour: 'numeric',
        minute: 'numeric',
        second: 'numeric',
        hour12: false
    });
    document.getElementById('currentDateTime').textContent = dateTimeString;
}

setInterval(updateDateTime, 1000);
